namespace SharpPDF.Enumerators {
	
	/// <summary>
	/// Enumerator that implements the style of a line.
	/// </summary>
	public enum PredefinedLineStyle
	{
		/// <summary>
		/// Normal line
		/// </summary>
		Normal,
		/// <summary>
		/// Thin Outlined line
		/// </summary>
		OutlinedSmall,
		/// <summary>
		/// Outlined line
		/// </summary>
		Outlined,
		/// <summary>
		/// Big Outlined line
		/// </summary>
		OutlinedBig
	}
}